export interface PixelRecipe {
  name: string;
  /**
   * Square frame side in pixels for the common case. For non-square (big multi-
   * tile) sprites, set `width`/`height` explicitly and `size` is ignored for
   * layout; each pixels row must then be `width` chars and there must be
   * `height` rows. Defaults: width = height = size.
   */
  size: number;
  /** Optional explicit frame width (px). Defaults to `size`. */
  width?: number;
  /** Optional explicit frame height (px). Defaults to `size`. */
  height?: number;
  pixels: readonly string[];
}

// ── Atlas sheet grouping ──────────────────────────────────────────────────────
// Design decision: derive the sheet from the frame-name prefix via an explicit
// map so that an unknown prefix fails loudly at build time (rather than landing
// in a silent default bucket). Adding a new prefix requires updating this map.
//
// Sheets (≈5–7; keeps the split practical):
//   characters — farmer/* + npc/*
//   buildings  — structure/*
//   terrain    — tile/*
//   crops      — crop/*
//   props      — decoration/*
//   items-ui   — fish/* + tool/* + indicator/* + debug/*
export const PREFIX_TO_SHEET: Readonly<Record<string, string>> = {
  "farmer":     "characters",
  "npc":        "characters",
  "structure":  "buildings",
  "tile":       "terrain",
  "crop":       "crops",
  "decoration": "props",
  "fish":       "items-ui",
  "tool":       "items-ui",
  "indicator":  "items-ui",
  "debug":      "items-ui",
};

/**
 * Derive the sheet id from a frame name (e.g. "farmer/conservative" → "characters").
 * Throws if the prefix is not registered in PREFIX_TO_SHEET so unknown frames
 * fail loudly at build time and at runtime.
 */
export function frameToSheetId(frame: string): string {
  const prefix = frame.split("/")[0];
  const sheet = PREFIX_TO_SHEET[prefix ?? ""];
  if (sheet === undefined) {
    throw new Error(`frameToSheetId: unknown prefix "${prefix ?? ""}" in frame "${frame}". Add it to PREFIX_TO_SHEET.`);
  }
  return sheet;
}

/** Resolved pixel width of a recipe (explicit `width`, else square `size`). */
export function recipeWidth(r: PixelRecipe): number {
  return r.width ?? r.size;
}

/** Resolved pixel height of a recipe (explicit `height`, else square `size`). */
export function recipeHeight(r: PixelRecipe): number {
  return r.height ?? r.size;
}

// Palette: Endesga-32 (EDG32) by Endesga — https://lospec.com/palette-list/endesga-32
// Every swatch below is one of the 32 EDG32 colors. The mapping is curated (not
// raw nearest-RGB) so grass stays green, the ocean stays blue with depth, and
// sand/wheat stay distinct (EDG32 has no teal, so a naive map muddied the grass).
const SWATCH: Record<string, [number, number, number, number]> = {
  ".": [0, 0, 0, 0],
  G: [62, 137, 72, 255],    // grass dark        #3e8948
  g: [99, 199, 77, 255],    // grass light       #63c74d
  D: [115, 62, 57, 255],    // wood dark         #733e39
  d: [184, 111, 80, 255],   // wood light        #b86f50
  S: [90, 105, 136, 255],   // structure blue    #5a6988
  s: [139, 155, 180, 255],  // structure blue lt #8b9bb4
  k: [24, 20, 37, 255],     // near-black        #181425
  w: [234, 212, 170, 255],  // cream / white     #ead4aa
  r: [190, 74, 47, 255],    // red / rust        #be4a2f
  l: [38, 92, 66, 255],     // leaf dark         #265c42
  L: [99, 199, 77, 255],    // leaf light        #63c74d
  W: [228, 166, 114, 255],  // tan / wheat       #e4a672
  m: [115, 62, 57, 255],    // trunk dark        #733e39
  M: [62, 39, 49, 255],     // trunk darker      #3e2731
  T: [234, 212, 170, 255],  // sand              #ead4aa
  c: [38, 92, 66, 255],     // grass base dark   #265c42
  C: [62, 137, 72, 255],    // grass base light  #3e8948
  q: [192, 203, 220, 255],  // stone light       #c0cbdc
  Q: [139, 155, 180, 255],  // stone dark        #8b9bb4
  o: [254, 174, 52, 255],   // gold              #feae34
  p: [215, 118, 67, 255],   // pumpkin           #d77643
  y: [254, 231, 97, 255],   // yellow            #fee761
  // Ocean shades for the out-of-region filler so the world reads as islands in
  // an ocean rather than a void beyond/between regions.
  v: [18, 78, 137, 255],    // ocean             #124e89
  V: [58, 68, 102, 255],    // ocean deep        #3a4466
  e: [0, 153, 219, 255],    // ocean foam        #0099db
};

export function colorOf(ch: string): [number, number, number, number] {
  const c = SWATCH[ch];
  if (!c) throw new Error(`Unknown swatch char: ${ch}`);
  return c;
}

