/** Character used to render any code point outside the covered range. */
export const FALLBACK_CHAR = "?";

/** Rows for `char`, falling back to '?' for anything outside printable ASCII. */
export function glyphRows(char: string): GlyphRows {
  return G[char] ?? G[FALLBACK_CHAR]!;
}

/** Every covered character in code-point order — drives the deterministic atlas layout. */
export function allChars(): string[] {
  const out: string[] = [];
  for (let cp = FIRST_CODEPOINT; cp <= LAST_CODEPOINT; cp += 1) {
    out.push(String.fromCharCode(cp));
  }
  return out;
}
